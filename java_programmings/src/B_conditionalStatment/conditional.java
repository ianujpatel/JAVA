package B_conditionalStatment;

public class conditional {
    public static void main(String[] args) {
        System.out.println("hello");
        for(int i =0;i<5;i++){
            System.out.println("hello world");
        }
        int i = 0;
        while(i<5){
            System.out.println(i);
            i++;
        }
        int j=0;
        do{
            System.out.println(j);
            j=j+1;
        }while(j<5);
    }
}
